package com.arjuna.wisatasemarang.Model;

public class Wisata {
    private String nama;
    private double lat;
    private double lon;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public double isLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double isLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
